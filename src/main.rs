pub mod board;

use board::Board;

fn main() {
    let board_array: [[u8; 9]; 9] = [[3, 0, 7, 0, 1, 0, 2, 5, 0], [0, 2, 4, 6, 3, 0, 9, 1, 7], [1, 9, 8, 2, 0, 7, 0, 0, 0],
    [6, 8, 1, 7, 3, 0, 0, 9, 0], [0, 9, 2, 8, 0, 0, 7, 6, 0], [0, 0, 0, 9, 0, 6, 8, 1, 4], [8, 0, 3, 9, 2, 0, 0, 0, 6], 
    [0, 7, 9, 3, 0, 6, 2, 8, 5], [5, 0, 0, 7, 8, 1, 0, 3, 0]];

    let mut board = Board { board: board_array };

    println!("{board:?}");
    println!("{:?}", board.solve());
}


